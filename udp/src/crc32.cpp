uint32_t calculateCRC32(const char* data, size_t length) {
    uint32_t crc = 0xFFFFFFFF;
    for (size_t i = 0; i < length; ++i) {
        crc = (crc >> 8) ^
              crc32_table[(crc & 0xFF) ^ static_cast<uint8_t>(data[i])];
    }
    return crc ^ 0xFFFFFFFF;
}